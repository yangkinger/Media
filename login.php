    // 示例：验证用户名和密码（可以替换为数据库验证）
    $validUsername = 'admin';
    $validPassword = file_get_contents('admin.txt');

    if ($username === $validUsername && $password === $validPassword) {
        // 设置会话变量
        $_SESSION['loggedin'] = true;
        $_SESSION['username'] = $username;

        // 跳转到频道管理页面
        header('Location: live.php');
        exit;
